
// Let's start with the basics
// alt + tab => switch between applications
// ctrl + c => copy
// ctrl + v => paste
// ctrl + z => undo
// ctrl + y => redo
// ctrl + x => cut
// shift + home => highlight from cursor to beginning of line
// shift + end => highlight from cursor to end of line
// ctrl + l => select current line
// hold shift + arrow key left/right to highlight specific section

// alt + arrow key up/down => move line or down
// shift + alt + arrow key down => copy lines
// shift + ctrl + l => all instances of word in file
// ctrl + ] => add indent to multiple lines
// ctrl + [ => remove indent from multiple
// ctrl + p => go to file
// ctrl + f => find instance of word in file
// ctrl + g => go to specific line
